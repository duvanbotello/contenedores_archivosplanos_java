    private void cargar() {
        tb1.setModel(modelo_tproyectos);
        modelo_tproyectos.setRowCount(0);
        tb1.setRowHeight(30);
        tb1.setDefaultRenderer(Object.class, new Render());
        JButton btn1 = new JButton("Asignar");
        btn1.setName("btm_Asignar");

        modelo_tproyectos.addColumn("Documento");
        modelo_tproyectos.addColumn("Nombre");
        modelo_tproyectos.addColumn("Acender");

        Object[] file = new Object[tb1.getColumnCount()];
        for (Abs_empleados e : metodos.lista_empleados) {
            file[0] = e.getDocumento();
            file[1] = e.getNombre_emple();
            file[2] = btn1;
            modelo_tproyectos.addRow(file);
        }
    }

