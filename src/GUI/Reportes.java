    private void reporte_segun_genero(int genero) {
        File reporte_empleado_sueldo = new File("src/Reportes/Reporte_Empleado_segungenero.xls");
        try {
            reporte_empleado_sueldo.delete();
            reporte_empleado_sueldo.createNewFile();
            BufferedWriter Fescribe = new BufferedWriter(new OutputStreamWriter(new FileOutputStream(reporte_empleado_sueldo, true), "utf-8"));
            Fescribe.write("-----------------------------REPORTE DE EMPLEADO SEGUN GENERO Y CON MAS DE 3 HIJOS Y CON SUELDO MENOR A $2.000.000----------------------------------------" + "\r\n");
            Fescribe.write("DOCUMENTO" + "\t" + "NOMBRE EMPLEADO" + "\t" + "NUM SEGURIDAD" + "\t" + "DIRECCION" + "\t" + "SUELDO" + "\t" + "GENERO" + "\t" + "HIJOS" + "\t"
                    + "FECHA DE NACI" + "\t" + "DEPARTAMENTO" + "\r\n");
            for (Abstracciones.Abs_empleados e : Metodos.metodos.lista_empleados) {

                if (e.getSueldo() <= 2000000 && e.getGenero() == genero && e.getNumero_hijos() > 3) {
                    String fecha1 = new SimpleDateFormat("dd/MM/yyyy").format(e.getFecha_nacimiento());
                    Fescribe.write(e.getDocumento() + "\t" + e.getNombre_emple() + "\t" + e.getNum_seguridad_s() + "\t" + e.getDireccion()
                            + "\t" + e.getSueldo() + "\t" + e.getGenero() + "\t" + e.getNumero_hijos() + "\t" + fecha1 + "\t" + e.getDepartamento_asignado() + "\r\n");
                }

            }
            Fescribe.close();

            int option = JOptionPane.showConfirmDialog(null, "¿Desea Abrir el archivo?", "Abrir el archivo", JOptionPane.YES_NO_OPTION);
            if (option == JOptionPane.YES_OPTION) {
                Desktop.getDesktop().open(reporte_empleado_sueldo);
            }

        } catch (Exception e) {
        }
    }
