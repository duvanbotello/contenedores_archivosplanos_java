    private void tb1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb1MouseClicked
        int seleccion = tb1.rowAtPoint(evt.getPoint());
        String codigo1 = String.valueOf(tb1.getValueAt(seleccion, 1));
        long codigo = Long.parseLong(codigo1);

        //CON ESTO AGARRO LA POSICION X Y LA Y PARA SABER LA UBICACION
        int column = tb1.getColumnModel().getColumnIndexAtX(evt.getX());
        int row = evt.getY() / tb1.getRowHeight();

        if (row < tb1.getRowCount() && row >= 0 && column < tb1.getColumnCount() && column >= 0) {
            Object value = tb1.getValueAt(row, column);
            //SI LA POCICION DONDE SE DIO CLIK ES UN BOTON SE PUEDE EJECUTAR EL CODIGO
            if (value instanceof JButton) {
                ((JButton) value).doClick();
                JButton boton = (JButton) value;

                if (boton.getName().equals("btm_reporte")) {
                    File reporte_departamentos = new File("src/Reportes/Reporte_proyectos_departamentos.xls");
                    try {
                        reporte_departamentos.delete();
                        reporte_departamentos.createNewFile();
                        BufferedWriter Fescribe = new BufferedWriter(new OutputStreamWriter(new FileOutputStream(reporte_departamentos, true), "utf-8"));
                        for (Abs_departamentos d : metodos.lista_departamentos) {
                            if (codigo == d.getCodigo()) {
                                for (Abs_proyectos p : d.getListaproyecto()) {
                                    String fechai = new SimpleDateFormat("dd/MM/yyyy").format(p.getFecha_i());
                                    String fechaf = new SimpleDateFormat("dd/MM/yyyy").format(p.getFecha_f());
                                    Fescribe.write(p.getNumero_empleados() + "\t" + p.getNombre() + "\t" + p.getDescripcion() + "\t" + fechai + "\t" + fechaf
                                            + "\t" + p.getDoc_empleado() + "\r\n");
                                }
                            }
                        }
                        Fescribe.close();

                        int option = JOptionPane.showConfirmDialog(null, "¿Desea Abrir el archivo?", "Abrir el archivo", JOptionPane.YES_NO_OPTION);
                        if (option == JOptionPane.YES_OPTION) {
                            Desktop.getDesktop().open(reporte_departamentos);
                        }

                    } catch (Exception e) {
                    }

                }

            }

        }
    }//GEN-LAST:event_tb1MouseClicked

