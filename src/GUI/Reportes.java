    private void btm_reporte_fechaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btm_reporte_fechaActionPerformed
        if (fecha_inicial.getDate() != null) {
            if (fecha_final.getDate() != null) {
                SimpleDateFormat fecha = new SimpleDateFormat("dd/MM/yyyy");
                File reporte_proyectos = new File("src/Reportes/Reporte_proyectos_fechas.xls");
                String fecha_i = new SimpleDateFormat("dd/MM/yyyy").format(fecha_inicial.getDate());
                String fecha_f = new SimpleDateFormat("dd/MM/yyyy").format(fecha_final.getDate());

                try {
                    reporte_proyectos.delete();
                    reporte_proyectos.createNewFile();
                    BufferedWriter Fescribe = new BufferedWriter(new OutputStreamWriter(new FileOutputStream(reporte_proyectos, true), "utf-8"));
                    Fescribe.write("------------------------------------------REPORTE DE PROYECTOS RANGO DE FECHAS-----------------------------------------------" + "\r\n");
                    Fescribe.write("ID DE PROYECTO" + "\t" + "NOMBRE PROYECTO" + "\t" + "DESCIPCION" + "\t" + "FECHA INICIAL" + "\t" + "FECHA FINAL" + "\t" + "DOCUMENTO EMPELADO" + "\r\n");
                    for (Abs_proyectos p : metodos.lista_proyectos) {

                        try {
                            Date fecha_ini = fecha.parse(fecha_i);
                            Date fecha_fin = fecha.parse(fecha_f);

                            if (p.getFecha_i().compareTo(fecha_ini) >= 0 && p.getFecha_f().compareTo(fecha_fin) <= 0) {

                                String fechai = new SimpleDateFormat("dd/MM/yyyy").format(p.getFecha_i());
                                String fechaf = new SimpleDateFormat("dd/MM/yyyy").format(p.getFecha_f());
                                Fescribe.write(p.getNumero_empleados() + "\t" + p.getNombre() + "\t" + p.getDescripcion() + "\t" + fechai + "\t" + fechaf
                                        + "\t" + p.getDoc_empleado() + "\r\n");

                            }

                        } catch (Exception e) {
                        }

                    }

                    Fescribe.close();

                    int option = JOptionPane.showConfirmDialog(null, "¿Desea Abrir el archivo?", "Abrir el archivo", JOptionPane.YES_NO_OPTION);
                    if (option == JOptionPane.YES_OPTION) {
                        Desktop.getDesktop().open(reporte_proyectos);
                    }
                } catch (Exception e) {
                }

            } else {
                JOptionPane.showMessageDialog(null, "Por favor Ingrese Fecha Final");
            }

        } else {
            JOptionPane.showMessageDialog(null, "Por favor Ingrese Fecha Inicial");
        }
    }//GEN-LAST:event_btm_reporte_fechaActionPerformed


